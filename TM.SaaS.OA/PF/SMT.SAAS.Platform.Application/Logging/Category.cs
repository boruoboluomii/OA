
// 内容摘要: 定义<see cref="Log"/>类别
namespace SMT.SAAS.Platform.Logging
{
	/// <summary>
	/// 定义<see cref="Log"/>类别
	/// </summary>
	public enum Category : int
	{
		/// <summary>
		/// 调试信息
		/// </summary>
		Debug,
		/// <summary>
		/// 异常信息
		/// </summary>
		Exception,
		/// <summary>
		/// 消息信息
		/// </summary>
		Info,
		/// <summary>
		/// 警告信息
		/// </summary>
		Warn,
	}
}
